// 9. 自費項目計算器邏輯 (支援 LocalStorage)
// ==========================================================================
let selectedActivities = [];

function initBudgetCalculator() {
    const stored = localStorage.getItem("selected_selfpaid_activities");
    if (stored) {
        selectedActivities = JSON.parse(stored);
    }
    
    renderBudgetList();
    calculateBudget();
}

function renderBudgetList() {
    const container = document.getElementById("budget-items-list");
    container.innerHTML = "";
    
    budgetActivities.forEach(act => {
        const item = document.createElement("div");
        const isChecked = selectedActivities.includes(act.id);
        item.className = "budget-item";
        
        item.innerHTML = `
            <input type="checkbox" class="todo-checkbox" ${isChecked ? "checked" : ""} style="margin-top: 4px;">
            <div class="budget-item-info">
                <div class="budget-item-title">${act.name}</div>
                <div class="budget-item-desc">${act.desc}</div>
            </div>
            <div class="budget-item-price">${act.price} EUR</div>
        `;
        
        const checkbox = item.querySelector("input[type='checkbox']");
        
        const toggleHandler = () => {
            const index = selectedActivities.indexOf(act.id);
            if (index > -1) {
                selectedActivities.splice(index, 1);
                checkbox.checked = false;
            } else {
                selectedActivities.push(act.id);
                checkbox.checked = true;
            }
            localStorage.setItem("selected_selfpaid_activities", JSON.stringify(selectedActivities));
            calculateBudget();
        };
        
        checkbox.addEventListener("change", toggleHandler);
        item.addEventListener("click", (e) => {
            if (e.target !== checkbox) toggleHandler();
        });
        
        container.appendChild(item);
    });
}

function calculateBudget() {
    let totalEur = 0;
    
    selectedActivities.forEach(id => {
        const act = budgetActivities.find(a => a.id === id);
        if (act) totalEur += act.price;
    });
    
    const rate = 37;
    const totalTwd = totalEur * rate;
    
    document.getElementById("total-eur").textContent = `${totalEur} EUR`;
    document.getElementById("total-twd").textContent = `${totalTwd.toLocaleString()} TWD`;
}

