function openAttractionModal(attrId) {
    const modal = document.getElementById("story-modal");
    const attr = attractionsData.find(a => a.id === attrId);
    
    if (!attr) return;
    
    const countryNames = {
        slovakia: "斯洛伐克",
        hungary: "匈牙利",
        austria: "奧地利",
        germany: "德國",
        czech: "捷克"
    };

    document.getElementById("modal-country").textContent = countryNames[attr.country];
    document.getElementById("modal-title").textContent = attr.name;
    document.getElementById("modal-hierarchy").innerHTML = `<i class="fa-solid fa-globe"></i> ${countryNames[attr.country]} ──> ${attr.city} ──> ${attr.name}`;
    document.getElementById("modal-intro-text").textContent = attr.introduction;
    document.getElementById("modal-legend-text").textContent = attr.legend;
    
    // 預設切換到第一個 Tab
    document.querySelectorAll(".modal-tab-btn").forEach(btn => btn.classList.remove("active"));
    document.querySelectorAll(".modal-tab-content").forEach(content => content.classList.remove("active"));
    
    document.querySelector('[data-tab="tab-intro"]').classList.add("active");
    document.getElementById("tab-intro").classList.add("active");

    modal.classList.add("active");
    document.body.style.overflow = "hidden"; // 阻止背景滾動
}

function setupModalEvents() {
    const modal = document.getElementById("story-modal");
    const closeBtn = document.getElementById("modal-close-btn");
    
    closeBtn.addEventListener("click", closeModal);
    modal.addEventListener("click", (e) => {
        if (e.target === modal) closeModal();
    });
    
    // Tab 切換
    const tabButtons = document.querySelectorAll(".modal-tab-btn");
    tabButtons.forEach(btn => {
        btn.addEventListener("click", () => {
            const tabId = btn.getAttribute("data-tab");
            
            tabButtons.forEach(b => b.classList.remove("active"));
            document.querySelectorAll(".modal-tab-content").forEach(c => c.classList.remove("active"));
            
            btn.classList.add("active");
            document.getElementById(tabId).classList.add("active");
        });
    });
}

function closeModal() {
    document.getElementById("story-modal").classList.remove("active");
    document.body.style.overflow = "auto";
}

