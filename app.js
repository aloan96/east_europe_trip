let userChecklist = {};

function initChecklist() {
    const stored = localStorage.getItem("travel_packing_checklist");
    if (stored) {
        userChecklist = JSON.parse(stored);
    } else {
        userChecklist = JSON.parse(JSON.stringify(defaultChecklist));
        saveChecklist();
    }
    
    renderChecklist();
    
    // 新增自訂打包按鈕監聽
    document.getElementById("add-todo-btn").addEventListener("click", addUserTodo);
    document.getElementById("new-todo-input").addEventListener("keypress", (e) => {
        if (e.key === "Enter") addUserTodo();
    });
}

function saveChecklist() {
    localStorage.setItem("travel_packing_checklist", JSON.stringify(userChecklist));
}

function renderChecklist() {
    const container = document.getElementById("checklist-groups");
    container.innerHTML = "";
    
    Object.keys(userChecklist).forEach(category => {
        const group = document.createElement("div");
        group.className = "checklist-group";
        
        const title = document.createElement("div");
        title.className = "checklist-group-title";
        
        // 分類圖示
        let icon = "fa-tags";
        if (category.includes("證件")) icon = "fa-passport";
        else if (category.includes("電子")) icon = "fa-laptop-code";
        else if (category.includes("衣物")) icon = "fa-shirt";
        else if (category.includes("藥品") || category.includes("自訂")) icon = "fa-kit-medical";
        
        title.innerHTML = `<i class="fa-solid ${icon}"></i> ${category}`;
        group.appendChild(title);
        
        const itemsWrapper = document.createElement("div");
        itemsWrapper.className = "checklist-items-container";
        
        userChecklist[category].forEach((item, index) => {
            const itemDiv = document.createElement("div");
            itemDiv.className = `todo-item ${item.completed ? "completed" : ""}`;
            
            itemDiv.innerHTML = `
                <div class="todo-item-left">
                    <input type="checkbox" class="todo-checkbox" ${item.completed ? "checked" : ""}>
                    <span class="todo-label">${item.text}</span>
                </div>
                <button class="todo-delete-btn"><i class="fa-solid fa-trash-can"></i></button>
            `;
            
            // 點擊勾選邏輯
            const checkbox = itemDiv.querySelector(".todo-checkbox");
            const leftClick = itemDiv.querySelector(".todo-item-left");
            
            const toggleHandler = () => {
                item.completed = !item.completed;
                saveChecklist();
                renderChecklist();
            };
            
            checkbox.addEventListener("change", toggleHandler);
            leftClick.addEventListener("click", (e) => {
                if (e.target !== checkbox) toggleHandler();
            });
            
            // 刪除邏輯
            const delBtn = itemDiv.querySelector(".todo-delete-btn");
            delBtn.addEventListener("click", () => {
                userChecklist[category].splice(index, 1);
                // 若分類空了且非預設，可考慮保留或刪除，在此直接保留空陣列
                saveChecklist();
                renderChecklist();
            });
            
            itemsWrapper.appendChild(itemDiv);
        });
        
        group.appendChild(itemsWrapper);
        container.appendChild(group);
    });
}

function addUserTodo() {
    const input = document.getElementById("new-todo-input");
    const val = input.value.trim();
    
    if (!val) return;
    
    // 預設加到「自訂項目」分類中
    const catName = "自訂備忘項目";
    if (!userChecklist[catName]) {
        userChecklist[catName] = [];
    }
    
    userChecklist[catName].push({ text: val, completed: false });
    input.value = "";
    
    saveChecklist();
    renderChecklist();
}

