// 導覽標籤切換
function setupNavigation() {
    const navButtons = document.querySelectorAll(".nav-btn");
    const panels = document.querySelectorAll(".tab-panel");
    
    navButtons.forEach(btn => {
        btn.addEventListener("click", () => {
            const targetId = btn.getAttribute("data-target");
            
            navButtons.forEach(b => b.classList.remove("active"));
            panels.forEach(p => p.classList.remove("active"));
            
            btn.classList.add("active");
            document.getElementById(targetId).classList.add("active");
            
            // 點擊導覽時，平滑滾動到內容頂端
            window.scrollTo({ top: 350, behavior: "smooth" });
        });
    });
}

// 渲染每日行程天數按鈕
function renderDaySelector() {
    const selector = document.getElementById("day-selector");
    selector.innerHTML = "";
    
    daysData.forEach(dayObj => {
        const btn = document.createElement("button");
        btn.className = `day-btn ${dayObj.day === 1 ? "active" : ""}`;
        btn.innerHTML = `D${dayObj.day}<br><span style="font-size:0.65rem; font-weight:normal;">${dayObj.day === 1 ? "出發" : "Day " + dayObj.day}</span>`;
        btn.addEventListener("click", () => {
            document.querySelectorAll(".day-btn").forEach(b => b.classList.remove("active"));
            btn.classList.add("active");
            showDayDetails(dayObj.day);
        });
        selector.appendChild(btn);
    });
}

// 顯示每日詳細行程卡片
function showDayDetails(dayNum) {
    const card = document.getElementById("day-detail-card");
    const dayObj = daysData.find(d => d.day === dayNum);
    
    if (!dayObj) return;
    
    // 將亮點標籤對應至景點資料
    const highlightsHtml = dayObj.highlights.map(hlKey => {
        const attraction = attractionsData.find(a => a.id === hlKey);
        if (attraction) {
            return `<span class="highlight-tag" onclick="openAttractionModal('${attraction.id}')"><i class="fa-solid fa-map-pin"></i> ${attraction.name}</span>`;
        }
        return `<span class="highlight-tag"><i class="fa-solid fa-star"></i> ${hlKey}</span>`;
    }).join("");

    card.innerHTML = `
        <div class="timeline-title-area">
            <div>
                <span class="day-num-tag"><i class="fa-solid fa-calendar-day"></i> 第 ${dayObj.day} 天 ── ${dayObj.date}</span>
                <div class="day-route">${dayObj.route}</div>
            </div>
            <div style="font-size:0.9rem; color:var(--text-secondary); display:flex; align-items:center; gap:6px;">
                <i class="fa-solid fa-bus"></i> 交通：${dayObj.transport}
            </div>
        </div>
        
        <p class="day-description-para">${dayObj.description}</p>
        
        <div class="timeline-info-grid">
            <div class="info-column">
                <h4><i class="fa-solid fa-utensils"></i> 每日三餐</h4>
                <ul>
                    <li><strong>早餐</strong>：${dayObj.meals.breakfast}</li>
                    <li><strong>午餐</strong>：${dayObj.meals.lunch}</li>
                    <li><strong>晚餐</strong>：${dayObj.meals.dinner}</li>
                </ul>
            </div>
            <div class="info-column">
                <h4><i class="fa-solid fa-bed"></i> 住宿飯店</h4>
                <div class="hotel-info-box">
                    <div class="hotel-name"><i class="fa-solid fa-hotel"></i> ${dayObj.hotel.name}</div>
                    <div class="hotel-addr"><i class="fa-solid fa-map-location-dot"></i> ${dayObj.hotel.address}</div>
                    <div class="hotel-tel"><i class="fa-solid fa-phone"></i> 電話：${dayObj.hotel.phone}</div>
                </div>
            </div>
        </div>

        <div style="margin-top: 25px; border-top: 1px solid var(--border-glass); padding-top: 20px;">
            <div style="font-size:0.85rem; font-weight:bold; color:var(--text-secondary); margin-bottom:10px;"><i class="fa-solid fa-tags"></i> 今日景點亮點 (點擊查看故事傳說)：</div>
            <div class="day-highlights-tag-container">${highlightsHtml}</div>
        </div>
    `;
}

